from dataclasses import dataclass
from typing import Optional, List


@dataclass
class MaterialClass:
    def __init__(
        self,
        name: str,
        thermalConductivity: Optional[List[float]] = None,
        thermalConductivityDataRange: Optional[List[float]] = None,
        specificHeat: Optional[List[float]] = None,
        specificHeatDataRange: Optional[List[float]] = None,
        linearExpansion: Optional[List[float]] = None,
        linearExpansionDataRange: Optional[List[float]] = None,
        linearExpansionTlow: Optional[float] = None,
        youngsModulus: Optional[List[float]] = None,
        youngsModulusDataRange: Optional[List[float]] = None,
    ):

        self.name = name
        self.thermalConductivity = thermalConductivity
        self.thermalConductivityDataRange = thermalConductivityDataRange
        self.specificHeat = specificHeat
        self.specificHeatDataRange = specificHeatDataRange
        self.linearExpansion = linearExpansion
        self.linearExpansionDataRange = linearExpansionDataRange
        self.linearExpansionTlow = linearExpansionTlow
        self.youngsModulus = youngsModulus
        self.youngsModulusDataRange = youngsModulusDataRange


